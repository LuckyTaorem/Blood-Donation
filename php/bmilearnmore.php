        <p>Scientifically, as per the standards set by WHO, the accepted range of a normal BMI is 18.5 to 24.9.<br>Therefore, if you fall into this range, you are in a safe weight zone.<br><br>
If you are below 18.5, you are categorized underweight. And if you have a BMI of more than 25 but less than 29.9,<br>you are considered overweight.<br><br>
And with a BMI of more than 30, you are considered to fall into the obese category.
